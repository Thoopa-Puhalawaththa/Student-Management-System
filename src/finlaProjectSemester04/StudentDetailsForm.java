    public void courseTableShow(){
        JTableHeader header= courseTable.getTableHeader();
        header.setForeground(Color.DARK_GRAY);
        header.setFont(new Font(null,Font.BOLD,18));
      
        TableColumn col = courseTable.getColumnModel().getColumn(0);
        col.setPreferredWidth(130);
        TableColumn col2 = courseTable.getColumnModel().getColumn(1);
        col2.setPreferredWidth(350);
        
        
        
        
        try {
            DBConnection con = new DBConnection();
            ArrayList<GetData> ctdata =  con.getCourseDtailsToTable();
            DefaultTableModel model = (DefaultTableModel) courseTable.getModel();
            Object[] row = new Object[2];
            for(int i = 0;i<ctdata.size();i++){
                row[0] = ctdata.get(i).getcCode();
                row[1] = ctdata.get(i).getcName();
                model.addRow(row);
            }

        } catch (SQLException ex) {
            Logger.getLogger(StudentDetailsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
