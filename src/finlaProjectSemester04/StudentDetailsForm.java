   void showTable(ArrayList<GetData> dataList){
      DefaultTableModel model = (DefaultTableModel)studentDataTable.getModel();
      Object[] row = new Object[10];
      int count=0;
      for(int i=0;i<dataList.size();i++){
          row[0] = ++count;
          row[1] = dataList.get(i).getIndexNo();
          row[2] = dataList.get(i).getStuId();
          row[3] = dataList.get(i).getFullName();
          row[4] = dataList.get(i).getGender();
          row[5] = dataList.get(i).getMobile();
          row[6] = dataList.get(i).getStreem();
          row[7] = dataList.get(i).getFaculty();
          row[8] = dataList.get(i).getCourseName();
          row[9] = dataList.get(i).getCourseCode();  
          model.addRow(row);

      }     
   }
